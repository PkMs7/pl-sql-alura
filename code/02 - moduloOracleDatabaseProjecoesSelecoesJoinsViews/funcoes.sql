-- Funções de String

LOWER() -- Transforma todos os textos em letras minúsculas
UPPER() -- Transforma todos os textos em letras maiúsculas
INITCAP() -- Transforma a primeira letra de cada palavra em maiúscula
CONCAT() -- Concatena todas as informações
LPAD() -- Acrescenta caracteres na frente do texto
RPAD() --Acrescenta caracteres no final do texto
SUBSTR() -- Retira trechos de um texto maior
INSTR() -- Testa se uma determinada cadeia de caracteres está contida no texto
LTRIM() -- Retira espaços à esquerda do caractere
RTRIM() -- Retira espaços à direita do caractere
TRIM() -- Retira espaços à direita e à esquerda do campo
TRANSLATE() -- Traduz determinados caracteres de um nome para outros caracteres
REPLACE() -- Traduz uma cadeia de caracteres de um nome para outros caracteres

